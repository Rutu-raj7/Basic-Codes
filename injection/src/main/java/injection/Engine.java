package injection;

public class Engine {

	private double cc;
	private String name;
	
	
	public double getCc() {
		return cc;
	}
	public void setCc(double cc) {
		this.cc = cc;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Engine [cc=" + cc + ", name=" + name + "]";
	}
	
	
	
        
      
		
         
         
	
}
